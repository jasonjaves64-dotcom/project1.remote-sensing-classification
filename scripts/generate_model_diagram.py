def save_diagram():
    """保存架构图到文件"""
    diagram = generate_architecture_diagram()
    
    # 保存为文本文件
    with open("model_architecture.txt", "w", encoding="utf-8") as f:
        f.write(diagram)
    
    # 保存为 Markdown 文件
    md_content = f"""# FusionCropNetV5EDL 模型架构

```
{diagram}
```

## 架构说明

### 输入层
- **Opt Seq**: 光学时序数据 [Batch, Time, 10 channels, Height, Width]
- **SAR Seq**: SAR时序数据 [Batch, Time, 5 channels, Height, Width]
- **DEM**: 数字高程模型 [Batch, 5 channels, Height, Width]
- **DOY**: 日序信息 [Batch, Time]

### 编码器层
- **OpticalEncoder**: ResNet50 + FPN 提取光学特征
- **SAREncoder**: IRB模块 + FiLM条件调制提取SAR特征
- **DEMEncoder**: 局部特征 + 全局特征融合

### 时序编码器
- **TemporalEncoderStream**: Transformer编码器 + Fourier DOY编码

### 跨模态融合
- **CrossModalAttention**: 窗口注意力机制
- **DEMSpatialConditioner**: FiLM调制注入DEM几何约束
- **LateFusion**: 自适应门控融合

### EDL Head
输出 Dirichlet 参数 α，用于不确定性估计

### 不确定性指标
- **Probs**: 预测概率 p_k = α_k / S
- **Vacuity**: 数据不确定性 K / S
- **Dissonance**: 认知不确定性 1 - Σp²
- **ClassVar**: 每类方差 p(1-p)/(S+1)
"""
    
    with open("model_architecture.md", "w", encoding="utf-8") as f:
        f.write(md_content)
    
    print("模型架构图已保存为:")
    print("  - model_architecture.txt")
    print("  - model_architecture.md")

if __name__ == "__main__":
    save_diagram()
