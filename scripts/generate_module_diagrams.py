def save_diagrams():
    """保存架构图到文件"""
    # 数据预处理管道架构图
    preprocess_diagram = generate_preprocess_pipeline_diagram()
    with open("preprocess_pipeline_architecture.txt", "w", encoding="utf-8") as f:
        f.write(preprocess_diagram)
    print("数据预处理管道架构图已保存: preprocess_pipeline_architecture.txt")
    
    # 系统测试套件架构图
    test_diagram = generate_test_suite_diagram()
    with open("test_suite_architecture.txt", "w", encoding="utf-8") as f:
        f.write(test_diagram)
    print("系统测试套件架构图已保存: test_suite_architecture.txt")

if __name__ == "__main__":
    save_diagrams()
